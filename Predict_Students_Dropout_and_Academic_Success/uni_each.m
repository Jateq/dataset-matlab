
% Loop through all columns of data
for i = 1:size(data, 2)
    t = (1:length(data(:, i))); % X-axis as student index
    figure;
    plot(t, data(:, i)); 
    grid on
    xlabel('Student Index'); 
    ylabel(getColumnLabel(i)); %
    title(sprintf('Plot for Column %d', i)); % Title for each column
end


